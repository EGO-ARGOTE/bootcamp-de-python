# Sumas y restas (+ y -)
suma = 2024 + 1996
print(suma)

resta = 2024 - 1996
print(resta)

# Multiplicacion y division (* y /)
multiplicacion = 23 * 7
print(multiplicacion)

division_entera = 8 / 2
print(division_entera)

# Potencia (**)
potencia = 2 ** 3
print(potencia)

# Division entera (//)
division = 12 // 4
print(division)

# Modulo (%)
modulo = 12 % 4
print(modulo)

tipo_de_dato = type(division_entera)
print(tipo_de_dato)